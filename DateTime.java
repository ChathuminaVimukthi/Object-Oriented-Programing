package vehicle.park;

//model DataType class for date and time
public class DateTime {
	private int year;
	private int month;
	private int date;
	private int hours;

	//Constructor to Set Values
	public DateTime(int year, int month, int date, int hours) {
		super();
		this.year = year;
		this.month = month;
		this.date = date;
		this.hours = hours;
	}
	
}
